// --- Gemini-driven UI Construction and Rendering Layer ---
// This object replaces traditional UI frameworks and JSX. It provides
// primitive functions for creating and manipulating DOM elements based
// on directives received from the GeminiContentGenerator and other agents.
const GeminiUIBuilder = {
    // Fundamental operation: creating a DOM element.
    createElement: function(tag, classes, textContent, attributes) {
        const element = document.createElement(tag);
        if (classes) {
            element.className = classes;
        }
        if (textContent) {
            element.textContent = textContent;
        }
        if (attributes) {
            for (const key in attributes) {
                // Ensure only own properties are considered.
                if (Object.prototype.hasOwnProperty.call(attributes, key)) {
                    element.setAttribute(key, attributes[key]);
                }
            }
        }
        return element;
    },

    // Helper for appending multiple child elements.
    appendChildren: function(parent, children) {
        children.forEach(child => {
            if (child instanceof Node) { // Ensure it's a DOM node
                parent.appendChild(child);
            }
        });
        return parent;
    },

    // Constructs a clickable navigation link, replacing React Router's Link.
    createNavigationLink: function(path, text, clickHandler) {
        // Uses hash-based routing for simplicity and "no dependencies".
        const linkElement = this.createElement('a', 'gemini-navigation-link', text, { 'href': `#${path}` });
        linkElement.addEventListener('click', (event) => {
            event.preventDefault(); // Prevent default browser hash navigation.
            clickHandler(path); // Delegate navigation logic to GeminiNavigationHandler.
        });
        return linkElement;
    },

    // Generates a structured page header, replacing custom PageHeader components.
    createPageHeader: function(titleText, contentChildren) {
        const headerContainer = this.createElement('div', 'gemini-page-header-container');
        const headerTitle = this.createElement('h1', 'gemini-page-header-title', titleText);
        this.appendChildren(headerContainer, [headerTitle]);
        if (contentChildren && contentChildren.length > 0) {
            const contentWrapper = this.createElement('div', 'gemini-page-header-content-wrapper');
            this.appendChildren(contentWrapper, contentChildren);
            this.appendChildren(headerContainer, [contentWrapper]);
        }
        return headerContainer;
    },

    // Constructs a detailed block for displaying flow-specific content with steps.
    createFlowContentBlock: function(headerText, stepsArray, footerText) {
        const flowBlock = this.createElement('div', 'gemini-flow-content-block');
        this.appendChildren(flowBlock, [
            this.createElement('h2', 'gemini-flow-content-header', headerText),
            this.createElement('ul', 'gemini-flow-steps-list')
        ]);

        const stepsList = flowBlock.querySelector('.gemini-flow-steps-list');
        stepsArray.forEach(step => {
            this.appendChildren(stepsList, [
                this.createElement('li', 'gemini-flow-step-item', step)
            ]);
        });

        this.appendChildren(flowBlock, [
            this.createElement('p', 'gemini-flow-content-footer', footerText)
        ]);
        return flowBlock;
    }
};

// --- Gemini-driven Navigation and Routing Layer ---
// This object manages the application's internal state regarding the current
// view/path, and orchestrates transitions based on user actions or programmatic calls.
// It replaces `react-router-dom`'s `Route` and `Switch` components.
const GeminiNavigationHandler = {
    currentPath: '',
    pathChangeListener: null, // Callback function invoked when the path changes.
    routeDefinitions: [],     // Stores the mapping from paths to conceptual rendering components.

    // Initializes the navigation system with defined routes.
    initialize: function(routeDefs) {
        this.routeDefinitions = routeDefs;
        // Listen for browser's hash changes to support back/forward buttons.
        window.addEventListener('hashchange', this.handleHashChange.bind(this));
        // Set initial path based on URL hash or default to root.
        this.currentPath = window.location.hash.substring(1) || '/';
    },

    // Handles changes to the URL hash, updating internal state and notifying listeners.
    handleHashChange: function() {
        const newPath = window.location.hash.substring(1) || '/';
        if (this.currentPath !== newPath) {
            this.currentPath = newPath;
            if (this.pathChangeListener) {
                this.pathChangeListener(newPath);
            }
        }
    },

    // Programmatically navigates to a new path.
    navigateTo: function(path) {
        if (this.currentPath !== path) {
            // Update browser hash, which will trigger handleHashChange.
            window.location.hash = path;
            // For immediate internal consistency, also update currentPath.
            this.currentPath = path;
            if (this.pathChangeListener) {
                this.pathChangeListener(path);
            }
        }
    },

    // Determines the appropriate rendering component and extracts parameters for the current path.
    getCurrentRoute: function() {
        const path = this.currentPath;
        for (const route of this.routeDefinitions) {
            // Regex for matching exact paths or paths with dynamic segments like ':id'.
            // Ensures the entire path is matched.
            const routeRegex = new RegExp(`^${route.path.replace(/:\w+/g, '([^/]+)')}(?:/?)$`);
            const match = path.match(routeRegex);
            if (match) {
                const params = {};
                // Extract dynamic parameters if the route definition contains them.
                if (route.path.includes(':')) {
                    const paramNames = (route.path.match(/:\w+/g) || []).map(name => name.substring(1));
                    paramNames.forEach((name, index) => {
                        // match[0] is the full match, match[1] onwards are the captured groups.
                        params[name] = match[index + 1];
                    });
                }
                return {
                    component: route.component, // The conceptual renderer object.
                    params: params,
                    matchedPath: route.path    // The defined path pattern that matched.
                };
            }
        }
        return null; // No matching route found.
    }
};

