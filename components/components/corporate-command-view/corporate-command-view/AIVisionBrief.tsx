export const useAISimulationData = (intervalMs: number = 7000) => {
    const [latestInsight, setLatestInsight] = useState<AISimulatedInsight | null>(null);
    const [investmentMetrics, setInvestmentMetrics] = useState({
        currentPortfolioValue: 75000000000, // Starting with a hefty mock value for investment appeal.
        dailyGrowthRate: 0.0018, // A healthy daily growth rate.
        totalInsightsGenerated: 0,
        criticalResolutions: 0,
        systemsOptimized: 0,
        totalCostSavings: 0, // New metric
        totalRevenueImpact: 0, // New metric
        fraudPreventedCount: 0, // New metric
        fastSettlementsAchieved: 0 // New metric
    });

    useEffect(() => {
        let insightCount = 0;
        let criticalCount = 0;
        let optimizedCount = 0;
        let fraudCount = 0;
        let fastSettlementCount = 0;

        const updateData = () => {
            const newInsight = generateMockInsight(insightCount++);
            setLatestInsight(newInsight);

            setInvestmentMetrics(prev => {
                const newValue = prev.currentPortfolioValue * (1 + prev.dailyGrowthRate + (Math.random() * 0.0007));
                if (newInsight.impactScore > 750) criticalCount++;
                if (newInsight.impactScore > 500) optimizedCount++;

                if (newInsight.relatedConcepts.includes('Fraud Detection') || newInsight.relatedConcepts.includes('Payment Security')) {
                    fraudCount++;
                }
                if (newInsight.relatedConcepts.includes('Real-time Payments') && newInsight.relatedConcepts.includes('Latency Reduction')) {
                    fastSettlementCount++;
                }

                return {
                    currentPortfolioValue: parseFloat(newValue.toFixed(2)),
                    dailyGrowthRate: prev.dailyGrowthRate * (1 + (Math.random() - 0.5) * 0.0015),
                    totalInsightsGenerated: prev.totalInsightsGenerated + 1,
                    criticalResolutions: criticalCount,
                    systemsOptimized: optimizedCount,
                    totalCostSavings: prev.totalCostSavings + (newInsight.costSavingsMillions || 0),
                    totalRevenueImpact: prev.totalRevenueImpact + (newInsight.revenueImpactMillions || 0),
                    fraudPreventedCount: fraudCount,
                    fastSettlementsAchieved: fastSettlementCount
                };
            });
        };

        const intervalId = setInterval(updateData, intervalMs);
        updateData();

        return () => clearInterval(intervalId);
    }, [intervalMs]);

    return { latestInsight, investmentMetrics };
};

